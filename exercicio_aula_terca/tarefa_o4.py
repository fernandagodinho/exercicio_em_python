# Inicialize a variável contador
contador = 0

# Adicione 5 ao contador usando +=
contador += 5
print(f"O valor do contador após adicionar 5 é: {contador}")

# Multiplique o contador por 2 usando *=
contador *= 2
print(f"O valor do contador após multiplicar por 2 é: {contador}")

# Subtraia 3 do contador usando -=
contador -= 3
print(f"O valor do contador após subtrair 3 é: {contador}")
