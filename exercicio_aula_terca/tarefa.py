# Variável nome (tipo str)
nome = "fernanda"

# Variável idade (tipo int)
idade = 42

# Variável altura (tipo float)
altura = 1.65

# Uma lista com 3 frutas favoritas
frutas_favoritas = ["banana", "morango", "maçã"]

# Um dicionário com as chaves: nome, idade, altura
pessoa = {
    "nome": "fernanda",
    "idade": 42,
    "altura": 1.78,
}

# Uma tupla com 3 cores favoritas
cores_favoritas = ("azul", "verde", "amarelo")

# Imprimindo as variáveis para verificar os tipos e valores
print(f"Nome: {nome} (Tipo: {type(nome)})")
print(f"Idade: {idade} (Tipo: {type(idade)})")
print(f"Altura: {altura} (Tipo: {type(altura)})")
print(f"Frutas favoritas: {frutas_favoritas} (Tipo: {type(frutas_favoritas)})")
print(f"Pessoa: {pessoa} (Tipo: {type(pessoa)})")
print(f"Cores favoritas: {cores_favoritas} (Tipo: {type(cores_favoritas)})")